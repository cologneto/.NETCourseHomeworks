using System;


namespace _06.ConcatenateStrings
{
    class Program
    {
        static void Main()
        {
            Console.WriteLine("Enter first string:");
            string str1 = Console.ReadLine();
            Console.WriteLine("Enter second string:");
            string str2 = Console.ReadLine();
            Console.Write("The result is: " + str1 + "|" + str2);
            Console.WriteLine();

        }
    }
}
